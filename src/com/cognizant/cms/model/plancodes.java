package com.cognizant.cms.model;

public class plancodes {
	private String plan_id;
	private String plan_desc;
	private String plan_cov1;
	private String plan_cov2;
	private String plan_cov3;
	private String plan_cov4;
	private String plan_cov5;
	public plancodes(String plan_id, String plan_desc, String plan_cov1,
			String plan_cov2, String plan_cov3, String plan_cov4,
			String plan_cov5) {
		this.plan_id = plan_id;
		this.plan_desc = plan_desc;
		this.plan_cov1 = plan_cov1;
		this.plan_cov2 = plan_cov2;
		this.plan_cov3 = plan_cov3;
		this.plan_cov4 = plan_cov4;
		this.plan_cov5 = plan_cov5;
	}
	public plancodes()
	{
	}
	public String getPlan_id() {
		return plan_id;
	}
	public void setPlan_id(String plan_id) {
		this.plan_id = plan_id;
	}
	public String getPlan_desc() {
		return plan_desc;
	}
	public void setPlan_desc(String plan_desc) {
		this.plan_desc = plan_desc;
	}
	public String getPlan_cov1() {
		return plan_cov1;
	}
	public void setPlan_cov1(String plan_cov1) {
		this.plan_cov1 = plan_cov1;
	}
	public String getPlan_cov2() {
		return plan_cov2;
	}
	public void setPlan_cov2(String plan_cov2) {
		this.plan_cov2 = plan_cov2;
	}
	public String getPlan_cov3() {
		return plan_cov3;
	}
	public void setPlan_cov3(String plan_cov3) {
		this.plan_cov3 = plan_cov3;
	}
	public String getPlan_cov4() {
		return plan_cov4;
	}
	public void setPlan_cov4(String plan_cov4) {
		this.plan_cov4 = plan_cov4;
	}
	public String getPlan_cov5() {
		return plan_cov5;
	}
	public void setPlan_cov5(String plan_cov5) {
		this.plan_cov5 = plan_cov5;
	}
	}
	



